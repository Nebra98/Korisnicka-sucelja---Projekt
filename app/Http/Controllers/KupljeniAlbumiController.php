     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $kupljeni_album = new KupljeniAlbum();

        $kupljeni_album->user_id = $request->input('user_id');
        $kupljeni_album->album_id = $request->input('album_id');

        $kupljeni_album->save();
        return redirect('index');


    }

